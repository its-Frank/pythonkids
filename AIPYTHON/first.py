### Python Basics for AI: Notes for Kids (13-17)

#### 1. What is Python?
'''
- Python is a fun, easy-to-read programming language used for games, apps, and even Artificial Intelligence (AI)!
- AI is when computers learn to think a bit like humans, and Python helps us tell them what to do. 
'''

#### 2. Variables
'''
- Variables are like labeled boxes where you store things (numbers, words, etc.).
- You name the box and put something inside.
'''

#Example:
name = "Alex"  # A box called "name" with "Alex" inside
age = 15       # A box called "age" with 15 inside
print(name)    # Shows "Alex"
print(age)     # Shows 15


#### 3. Data Types
'''
- Variables can hold different types of "stuff":
  - **int**: Whole numbers (e.g., 5, 10, -3)
  - **float**: Decimal numbers (e.g., 3.14, 0.5)
  - **str**: Text or words (e.g., "Hello", "AI")
  - **bool**: True or False (like a yes/no switch)
'''
#Example:
height = 5.8      # float
score = 100       # int
message = "Cool!" # str
is_fun = True     # bool
print(height, score, message, is_fun)


#### 4. Operators
'''
- Operators are like tools for math or comparisons:
  - `+` (add), `-` (subtract), `*` (multiply), `/` (divide)
  - `==` (is equal?), `!=` (not equal), `<` (less than), `>` (greater than)
'''
#Example:
a = 10
b = 5
print(a + b)  # 15
print(a * b)  # 50
print(a == b) # False




